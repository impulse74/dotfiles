theme.bg_minimize   = "#6e6f70"
theme.bg_systray    = theme.bg_normal

theme.fg_normal     = "#f2f4f8"
theme.fg_focus      = "#161616"
theme.fg_urgent     = "#ffffff"
theme.fg_minimize   = "#161616"

theme.useless_gap   = dpi(0)
